 function MOplayPause() {
    if (Molybdenum.paused) {
        Molybdenum.play(); 
        document.getElementById('bod').style.backgroundColor = "rgb(1, 255, 18)";
        /*$('.pause-btn').show();
        $('.play-btn').hide();*/
    } else {
        Molybdenum.pause(); 
        document.getElementById('bod').style.backgroundColor = 'white';
        /*$('.play-btn').show();
        $('.pause-btn').hide();*/
    }
}
